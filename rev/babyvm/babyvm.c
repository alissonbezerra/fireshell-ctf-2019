void eval(int instr) {
    switch (instr) {
        case HLT: {
            running = false;
            printf("Program executed successfully!\n");
            break;
        }
        case PSH: {
    	    sp++;
	        stack[sp] = program[++ip];
	        break;
        }
        case POP: {
	        //int val_popped = stack[sp--];
	        //printf("popped %d\n", val_popped);
	        break;
	    }
	    case ADD: {
	        int a = stack[sp--];
	    	int b = stack[sp--];
	        int result = b + a;
	        sp++;
	        stack[sp] = result;
            //printf("%d - %d = %d\n", a, b, result);
	        break;
	    }
        case MUL: {
            int a = stack[sp--];
            int b = stack[sp--];
            int result = b * a;
            sp++;
            stack[sp] = result;
            //printf("%d - %d = %d\n", a, b, result);
            break;
        }
        case DIV: {
            int a = stack[sp--];
            int b = stack[sp--];
            int result = b / a;
            sp++;
            stack[sp] = result;
            //printf("%d - %d = %d\n", a, b, result);
            break;
        }
        case SUB: {
            int a = stack[sp--];
            int b = stack[sp--];
            int result = b - a;
            sp++;
            stack[sp] = result;
            //printf("%d - %d = %d\n", a, b, result);
            break;
        }
    }
}

